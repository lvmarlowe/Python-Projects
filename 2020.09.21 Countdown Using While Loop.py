# Countdown Using While Loop: Ringing in the New Year
# This program counts down from 10 and rings in the New Year using a "while" loop.
##########

count = 10  # Count starts at 10.
while count > 0:  # Evaluate whether count is greater than 0.
    print(count)  # If so, print count.
    count = count - 1  # Then, reduce count by 1.
print('Happy New Year! Goodbye forever, 2020!')  # When count reaches zero, welcome the New Year and bid farewell to 2020.
